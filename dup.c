#include "push_swap.h"

int	ft_dup(t_info *pass)
{
	int	i;
	int	j;

	i = 0;
	while (i < pass->total)
	{
		j = 1;
		while (i + j < pass->total)
		{
			if (pass->a[i] == pass->a[i + j])
				return (-1);
			++j;
		}
		++i;
	}
	return (0);
}
